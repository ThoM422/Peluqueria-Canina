    private void cargarTabla() {
        //1)Definir modelo de la tabla.
        DefaultTableModel modelo=new DefaultTableModel(){
            //filas y columnas no son editables.
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        //Establecer los nombres de las columnas.
        String titulos [] = {"ID","Color","Raza","Alergico","At. Esp.","Dueño","Cel"};
        modelo.setColumnIdentifiers(titulos); //Setea los nombres a la columna.
        
        //Carga de datos desde la BD.
        List<Mascota> listaMascotas = control.traerMascotas();
        
        //Recorrer la lista y mostrar cada uno de los elementos en la tabla.
        if(listaMascotas!=null){
            for(Mascota masco:listaMascotas){
                //Hay varios objetos distintos, por eso se utiliza "objeto".
                Object[] objeto={masco.getId(), masco.getColor(), masco.getRaza(), masco.getAlergico(),
                masco.getAtencion_especial(), masco.getUnDuenio().getNombre(),masco.getUnDuenio().getCelDuenio()};
                modelo.addRow(objeto);
            }
        }
        
        tabla.setModel(modelo);
    }
